//PrintEx.java
public class PrintEx {
    public static void main(String[] args) {
        //자바에서의 화면 출력
        int k = 10;
        char l = 'q';
        System.out.printf("1) %s에서의 화면출력 %d번\n", "JAVA", 1);//(\n)은 개행문자
        System.out.println("2) JAVA에서의 화면출력 2번");
        System.out.print("2) JAVA에서의 화면출력 3번\n");
        System.out.print("2) "+('a' + 1) + " b "+k+" "+l);//'a'는 ASC II 에서 97이므로 87이 출력 +연산 기호를 통해 연장가능

    }
}
